#ifndef __ORTHO_FRUSTUM_H
#define __ORTHO_FRUSTUM_H


// ----------------------------------------------------------------------------
// Standard includes
// ----------------------------------------------------------------------------
#include "Frustum.h"
#include "Sphere.h"


// ----------------------------------------------------------------------------
// Name: OrthoFrustum (class)
// Desc: Describes an orthographic frustum
// ----------------------------------------------------------------------------
class OrthoFrustum : public Frustum
{
public:
  // ------------------------------------------------------------------------
  // Constructor
  // ------------------------------------------------------------------------
  OrthoFrustum();

  // ------------------------------------------------------------------------
  // Set the frustum - which is an oriented box.
  // ------------------------------------------------------------------------
  void Set(const Sphere &focus, const Vec3 &dir, float length);

protected:
  // ------------------------------------------------------------------------
  // Private functions
  // ------------------------------------------------------------------------
  virtual void RecalculateFrustum() const;

  // ------------------------------------------------------------------------
  // Privates members
  // ------------------------------------------------------------------------
  Sphere  m_Focus;
  Vec3    m_Dir;
  float   m_Length;

}; // class OrthoFrustum


#endif // __ORTHO_FRUSTUM_H

