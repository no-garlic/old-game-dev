#ifndef _BOX_H
#define _BOX_H


// ----------------------------------------------------------------------------
// Standard includes
// ----------------------------------------------------------------------------
#include "Math/Types.h"
#include "Math/Vec3.h"


// ----------------------------------------------------------------------------
// Name: Box (struct)
// Desc: Class to define an axis aligned bounding box (aabb).
// ----------------------------------------------------------------------------
class Box
{
public:
    // ------------------------------------------------------------------------
    // The min and max extents of the box
    // ------------------------------------------------------------------------
    Vec3    Min;
    Vec3    Max;


    // ------------------------------------------------------------------------
    // Calculate the bounding box around two other boxes
    // ------------------------------------------------------------------------
    void Calculate(const Box &box1, const Box &box2);


}; // struct Box


#endif // _BOX_H

