#ifndef _COORDS_H
#define _COORDS_H


// ----------------------------------------------------------------------------
// Standard includes
// ----------------------------------------------------------------------------
#include "Math/Types.h"
#include "Math/Vec3.h"
#include "Math/Quat.h"


// ----------------------------------------------------------------------------
// Name: Coords (class)
// Desc: Manages a position and rotation as a vec3 and a quaternion
// ----------------------------------------------------------------------------
class Coords
{
public:
    // ------------------------------------------------------------------------
    // Constructors
    // ------------------------------------------------------------------------
    Coords()                                               { Identity(); }
    Coords(const Vec3 &v, const Quat &q) : xyz(v), quat(q) {             }

    
    // ------------------------------------------------------------------------
    // Reset the coords to identity
    // ------------------------------------------------------------------------
    Coords &Identity();


    // ------------------------------------------------------------------------
    // Coords members - position and rotation
    // ------------------------------------------------------------------------
    Vec3    xyz;
    Quat    quat;
}; // struct Coords


#endif // _COORDS_H

